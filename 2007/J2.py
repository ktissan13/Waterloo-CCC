phrase = str(input("Enter phrase: "))

if phrase.upper() == "CU":
    print("See you")
elif phrase == ":-)":
    print("I'm happy")
elif phrase == ":-(":
          print("I'm unhappy")
elif phrase == ";-)":
          print("wink")
elif phrase == ":-p":
          print("stick out my tongue")
elif phrase == "(~.~)":
          print("sleepy")
elif phrase.upper() == "TA":
          print("totally awesome")
elif phrase.upper() == "CCC":
    print("Canadian Computing Competition")
elif phrase.upper() == "CUZ":
    print("because")
elif phrase.upper() == "TY":
    print("thank you")
elif phrase.upper() == "YW":
    print("you're welcome")
elif phrase.upper() == "TTYL":
    print("talk to you later")
else:
    print("Invalid Input")
          
